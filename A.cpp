{
        int number;
        char status;
        Node* prev;
};

struct stack1
{
    Node* head;

    bool is_empty() {
        return head == nullptr;
    }

    void add(int number, char status) {
        Node *link = new Node;
        link->number = number;
        link->status = status;
        link->prev = head;
        head = link;
    }

    void pop() {
        if (is_empty()==0) {
            cout << head->number << '\n';
            Node *new_head = head;
            head = head->prev;
            delete new_head;
        }
    }
};

