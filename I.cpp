ll bs(ll women[], ll men[], ll n, ll i, ll mid) {
    ll left = -1;
    ll right = n;
    while (left < right - 1) {
        ll mid_index = (left + right) / 2;
        if (women[i] + men[mid_index] <= mid) {
            left = mid_index;
        }else{
            right = mid_index;
        }
    }
    return right;
}

ll counting_bs(ll women[], ll men[], ll n, ll k) {
    ll left = men[0] + women[0];
    ll right = men[n-1] + women[n-1];
    while (left < right - 1) {
        ll count_pairs_below = 0;
        ll mid = (left + right) / 2;
        for (ll i = 0; i < n; i++) {
            count_pairs_below += bs(women, men, n, i, mid);
        }
        if (count_pairs_below <= k - 1) {
            left = mid;
        }else{
            right = mid;
        }
    }
    return right;
}

void quicksort(ll* array, ll size) {
    ll index_left_side_element = 0;
    ll index_right_side_element = size - 1;

    if (size < 2) {
        return;
    }

    ll middle = array[size/2];

    while (index_left_side_element <= index_right_side_element) {
        while (array[index_left_side_element] < middle) {
            index_left_side_element++;
        }
        while (array[index_right_side_element] > middle) {
            index_right_side_element--;
        }
        if (index_left_side_element <= index_right_side_element) {
            if (index_left_side_element != index_right_side_element) {
                std::swap(array[index_left_side_element], array[index_right_side_element]);
            }
            index_left_side_element++;
            index_right_side_element--;
        }
    }
    quicksort(array,index_right_side_element+1);
    quicksort(array + index_left_side_element, size - index_left_side_element);
}


