struct Node
{
        int number;
        int days_before_better_task = -1;
        Node* prev;
        Node* next;
};

struct List
{
    Node* top = nullptr;
    Node* bottom = nullptr;
    bool non_decreasing = 1;
    bool checked = 0;

    bool is_empty() {
        return top == nullptr;
    }

    void add(int number) {
        Node *link = new Node;
        link->number = number;
        if (is_empty()) {
            top = bottom = link;
        } else {
        link->prev = top;
        top->next = link;
        top = link;
        if (!checked && top->number > top->prev->number) {
            checked = 1;
            non_decreasing = 0;
            }
        }
    }
};

signed main()
{
    List my_List;
    int n;
    cin >> n;
    for (int i = 0; i < n; i++) {
        int temp;
        cin >> temp;
        my_List.add(temp);
    }

    if (my_List.non_decreasing) {
        for (int i = 0; i < n; i++) {
            cout << -1 << " ";
        }
        return 0;
    }

    Node* currect = my_List.top->prev;
    while (currect != nullptr) {
        int count = 1;
        Node *compare = currect->next;
        while (compare != nullptr) {
            if ((currect->number) == (compare->number)) {
                if ((compare->days_before_better_task != -1)) {
                currect->days_before_better_task = compare->days_before_better_task + count;
                }
                break;
            }
            else if ((currect->number) < (compare->number)) {
                currect->days_before_better_task = count;
                break;
            }
            compare = compare->next;
            count++;
        }
        currect = currect->prev;
    }

    while (my_List.bottom != nullptr) {
        cout << my_List.bottom->days_before_better_task << ' ';
        my_List.bottom = my_List.bottom -> next;
    }
    return 0;
}
